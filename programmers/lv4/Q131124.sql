SELECT  A.MEMBER_NAME
        ,B.REVIEW_TEXT
        ,DATE_FORMAT(B.REVIEW_DATE, '%Y-%m-%d') AS REVIEW_DATE
FROM MEMBER_PROFILE A INNER JOIN REST_REVIEW B
ON (A.MEMBER_ID = B.MEMBER_ID)
WHERE A.MEMBER_ID IN (
    SELECT MEMBER_ID
    FROM REST_REVIEW
    GROUP BY MEMBER_ID
    HAVING COUNT(*) = (
        SELECT MAX(A.CNT)
        FROM (
            SELECT MEMBER_ID, COUNT(MEMBER_ID) AS CNT
            FROM REST_REVIEW B
            GROUP BY MEMBER_ID
        ) A
    )
)
ORDER BY REVIEW_DATE, REVIEW_TEXT


