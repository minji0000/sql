SELECT   DATE_FORMAT(A.DIFFERENTIATION_DATE, '%Y') AS YEAR
        ,A.SIZE_OF_COLONY
        ,CASE
            WHEN DATE_FORMAT(A.DIFFERENTIATION_DATE, '%Y') = '2019' THEN A.ID END 힝
FROM ECOLI_DATA A LEFT OUTER JOIN (SELECT  MAX(SIZE_OF_COLONY) AS MAX_SIZE
                                          ,DATE_FORMAT(DIFFERENTIATION_DATE, '%Y') AS YEAR
                                   FROM ECOLI_DATA A
                                   GROUP BY DATE_FORMAT(DIFFERENTIATION_DATE, '%Y')) B
ON DATE_FORMAT(DIFFERENTIATION_DATE, '%Y') = B.YEAR

